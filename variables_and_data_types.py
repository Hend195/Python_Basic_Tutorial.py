# Python Basics - Variables and Data Types

# -----------------------------
# Variables
# -----------------------------
# A variable is like a container to store data
name = "Sara"
age = 25
height = 1.65
is_student = True

# -----------------------------
# Data Types
# -----------------------------
# String: text enclosed in quotes
# Integer: whole numbers
# Float: decimal numbers
# Boolean: True or False

print("Name:", name)        # Output: Sara
print("Age:", age)          # Output: 25
print("Height:", height)    # Output: 1.65
print("Student:", is_student)  # Output: True

# -----------------------------
# Type Checking
# -----------------------------
print("The type of name is : " , (type(name)))    # <class 'str'>
print("The type of age is : " , (type(age)))    # <class 'int'>
print("The type of height is : " , (type(height)))    # <class 'float'>
print("The type of student is : " , (type(is_student)))    # <class 'bool'>

